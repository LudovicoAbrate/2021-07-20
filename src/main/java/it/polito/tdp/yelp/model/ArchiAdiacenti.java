package it.polito.tdp.yelp.model;

public class ArchiAdiacenti {
	
	String v;
	double peso;
	
	
	public ArchiAdiacenti(String v, double peso) {
		super();
		this.v = v;
		this.peso = peso;
	}
	public String getV() {
		return v;
	}
	public void setV(String v) {
		this.v = v;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	
	
}
